$(document).ready(function(){
    $("#btn_log_off").on('click',function(){
        $.confirm({
            theme:'dark',
            title:'Atención',
            content:'Está a punto de salir, ¿desea continuar?',
            buttons:{
                confirm:{
                    text:'Sí',
                    action:function(){
                        $.ajax({
                            url:'/auth/logout',
                            data:'aaa',
                            type:'GET',
                            success:function(response){
                                location.reload();
                            },
                            error:function(error){
                                console.log(error);
                            }
                        });
                    }
                },
                cancel:{
                    text:'No'
                }
            }
        });
    });
});
